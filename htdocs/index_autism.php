			<div class="section">
				<div class="name"><b>临床表现</b></div>
				<div class="part">
					<div>
						<div class="title"><b>1.语言障碍</b></div>
						<div class="content">
							<span>
								语言与交流障碍是孤独症的重要症状，是大多数儿童就诊的主要原因。<br>
								语言与交流障碍可以表现为多种形式，多数孤独症儿童有语言发育延迟或障碍，通常在两岁和三岁时仍然不会说话，或者在正常语言发育后出现语言倒退，在2～3岁以前有表达性语言，随着年龄增长逐渐减少，甚至完全丧失，终身沉默不语或在极少数情况下使用有限的语言。他们对语言的感受和表达运用能力均存在某种程度的障碍。
							</span>
						</div>
					</div>
					<div>
						<div class="title"><b>2.社会交往障碍</b></div>
						<div class="content">
							<span>
								患者不能与他人建立正常的人际关系。年幼时即表现出与别人无目光对视，表情贫乏，缺乏期待父母和他人拥抱、爱抚的表情或姿态，也无享受到爱抚时的愉快表情，甚至对父母和别人的拥抱、爱抚予以拒绝。分不清亲疏关系，对待亲人与对待其他人都是同样的态度。<br>不能与父母建立正常的依恋关系，患者与同龄儿童之间难以建立正常的伙伴关系，例如，在幼儿园多独处，不喜欢与同伴一起玩耍；看见一些儿童在一起兴致勃勃地做游戏时，没有去观看的兴趣或去参与的愿望。
							</span>
						</div>
					</div>
					<div>
						<div class="title"><b>3.兴趣范围狭窄和刻板的行为模式</b></div>
						<div class="content">
							<span>
								患者对于正常儿童所热衷的游戏、玩具都不感兴趣，而喜欢玩一些非玩具性的物品，如一个瓶盖，或观察转动的电风扇等，并且可以持续数十分钟、甚至几个小时而没有厌倦感。对玩具的主要特征不感兴趣，却十分关注非主要特征：患者固执地要求保持日常活动程序不变，如上床睡觉的时间、所盖的被子都要保持不变，外出时要走相同的路线等。若这些活动被制止或行为模式被改变，患者会表示出明显的不愉快和焦虑情绪，甚至出现反抗行为。患者可有重复刻板动作，如反复拍手、转圈、用舌舔墙壁、跺脚等。
							</span>
						</div>
					</div>
					<div>
						<div class="title"><b>4.智能障碍</b></div>
						<div class="content">
							<span>
								在孤独症儿童中，智力水平表现很不一致，少数患者在正常范围，大多数患者表现为不同程度的智力障碍。国内外研究表明，对孤独症儿童进行智力测验，发现50%左右的孤独症儿童为中度以上的智力缺陷（智商小于50），25%为轻度智力缺陷（智商为50～69），25%智力在正常（智商大于70），智力正常的被称为高功能孤独症。
							</span>
						</div>
					</div>
				</div>
			</div>

			<div class="section">
				<div class="name"><b>治疗</b></div>
				<div class="part">
					<div>
						<div class="title"><b>1.训练干预方法</b></div>
						<div class="content">
							<span>
								虽然目前孤独症的干预方法很多，但是大多缺乏循证医学的证据。尚无最优治疗方案，最佳的治疗方法应该是个体化的治疗。其中，教育和训练是最有效、最主要的治疗方法。目标是促进患者语言发育，提高社会交往能力，掌握基本生活技能和学习技能。孤独症患者在学龄前一般因不能适应普通幼儿园生活，而在家庭、特殊教育学校、医疗机构中接受教育和训练。学龄期以后患者的语言能力和社交能力会有所提高，部分患者可以到普通小学与同龄儿童一起接受教育，还有部分患者可能仍然留在特殊教育学校。<br>
								<br>
								目前国际上受主流医学推荐和使用的训练干预方法，为孤独症的规范化治疗提供了方向，这些主流方法主要有:<br>
								<br>
								<b>(1) 应用行为分析疗法 (ABA)</b><br>
								主张以行为主义原理和运用行为塑造原理，以正性强化为主促进孤独症儿童各项能力发展。训练强调高强度、个体化、系统化。<br>
								<br>
								<b>(2) 孤独症以及相关障碍儿童治疗教育课程 (TEACCH) 训练</b><br>
								该课程根据孤独症儿童能力和行为的特点设计个体化的训练内容，对患儿语言、交流以及感知觉运动等各方面所存在的缺陷有针对性地进行教育，核心是增进孤独症儿童对环境、教育和训练内容的理解和服从。<br>
								<br>
								<b>(3) 人际关系训练法</b><br>
								包括地板时光疗法(floor time)、 人际关系发展干预疗法(relationship development intervention，RDI)<br>
								<br>
								上述治疗方法在国内一些孤独症康复机构已开展，获取了较好的治疗效果，但还需要进一步研究论证。
							</span>
						</div>
					</div>
					<div>
						<div class="title"><b>2.药物治疗</b></div>
						<div class="content">
							<span>
								目前药物治疗尚无法改变孤独症的病程，也缺乏治疗核心症状的特异性药物，但药物可以改善患者的一些情绪和行为症状，如情绪不稳、注意缺陷和多动、冲动行为、攻击行为、自伤和自杀行为、抽动和强迫症状以及精神病性症状等，有利于维护患者自身或他人安全、顺利实施教育训练及心理治疗。
							</span>
						</div>
					</div>
				</div>
