Vue.component('tab-cards',{
	data : function(){
		return {
			tabs : {
				recommend : 'active'
				,hot : ''
			}
		}
	}
	,template : '<div class="TABCARDS clearfix">'
					+'<span :class="tabs.recommend" data-name="recommend" @click="activeClass">推荐</span>'
					+'<span :class="tabs.hot" data-name="hot" @click="activeClass">热榜</span>'
				+'</div>'
	,methods : {
		activeClass : function(e){
			for(key in this.tabs){
				this.tabs[key] = '';
				if(e.target.dataset.name == key){this.tabs[key] = 'active'}
			}
			this.type = e.target.dataset.name;
			ARTICLELIST.type = this.type;
		}
	}
});

var ARTICLELIST = new Vue({
	el : '._ARTICLELIST_'
	,data : {
		page : 1
		,pageSize : 15
		,type : 'recommend'
	}

	,created : function(){
		this.GET();
	}
	,methods : {
		showType : function(){
			console.log(this.type);
		}
		,GET : function(){
			this.createArticleListData();
			this.getArticleList();
		}
		,createArticleListData : function(){
			this.articleListData = {};
			this.articleListData.page = this.page;
			this.articleListData.pageSize = this.pageSize;
			this.articleListData.type = this.type;
		}
		,getArticleList : function(){
			var param = {url : '/personal/apply/front/stackArticle/list', data : this.articleListData};
			JOEDAR.doPostAjax(param,this.getArticleListFn);
		}
		,getArticleListFn : function(data){
			console.log(data);
		}
	}
});

